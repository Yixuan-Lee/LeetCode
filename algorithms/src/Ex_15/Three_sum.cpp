class Solution {
public:
    vector<vector<int>> threeSum(vector<int> &nums) {
        vector<vector<int>> res;
        if (nums.size() <= 2) {
            return res;
        }

        unsigned long l, r;
        unsigned long size = nums.size();
        set<vector<int>> setVec;    // sets are container that store unique elements
        int k = 0;

        std::sort(nums.begin(), nums.end());

        // parameters:
        // 1. pointer to the *array* (not vector) to be sorted
        // 2. number of elements in the array
        // 3. size in bytes of each element
        // 4. pointer to a function that compares two elements
        // qsort(nums, nums.size(), sizeof(int), compare);

        for (int i = 0; i < size - 2; i++) {
            l = i + 1;

            r = size - 1;
            while (l < r) {
                if (nums[i] + nums[l] + nums[r] == 0) {
                    //std::cout << nums[i] << " " << nums[l] << " " << nums[r] << std::endl;
                    vector<int> v {nums[i], nums[l], nums[r]};
//                    res.push_back(v);     // this will cause memory limited exceeded
                    if (setVec.count(v) == 0) {
                        setVec.insert(v);
                    }
                    l++;
                } else if (nums[i] + nums[l] + nums[r] < 0) {
                    l++;
                } else if (nums[i] + nums[l] + nums[r] > 0) {
                    r--;
                }
            }
        }

        // remove duplicated element in res using unique (this will cause memory limited exceeded)
//        std::sort(res.begin(), res.end());
//        res.erase(std::unique(res.begin(), res.end()), res.end());

        for (set<vector<int>>::iterator it = setVec.begin();
             it != setVec.end(); ++it) {
            res.push_back(*it);
        }

        return res;
    }
};

